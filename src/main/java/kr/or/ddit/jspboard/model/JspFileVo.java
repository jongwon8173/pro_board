public class JspFileVo {
	
	private int file_no ; 			// 첨부파일순번	  		널 여부   "N"
	private int posts_no ; 			// 게시글번호(ID)	    널 여부   "N"
	private  String file_upnm ; 	// 업로드첨부파일명	    널 여부   "N"
	private  String file_path ; 	// 첨부파일경로	    	널 여부   "N"
	private  String file_renm ; 	// 실제첨부파일명	    널 여부   "Y"
	private  String file_size ; 	// 첨부파일사이즈	    널 여부   "Y"
	
	
	public JspFileVo() {
		super();
		// TODO Auto-generated constructor stub
	}

	public JspFileVo(int file_no, int posts_no, String file_upnm,
			String file_path, String file_renm, String file_size) {
		super();
		this.file_no = file_no;
		this.posts_no = posts_no;
		this.file_upnm = file_upnm;
		this.file_path = file_path;
		this.file_renm = file_renm;
		this.file_size = file_size;
	}

	public int getFile_no() {
		return file_no;
	}

	public void setFile_no(int file_no) {
		this.file_no = file_no;
	}

	public int getPosts_no() {
		return posts_no;
	}

	public void setPosts_no(int posts_no) {
		this.posts_no = posts_no;
	}

	public String getFile_upnm() {
		return file_upnm;
	}

	public void setFile_upnm(String file_upnm) {
		this.file_upnm = file_upnm;
	}

	public String getFile_path() {
		return file_path;
	}

	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}

	public String getFile_renm() {
		return file_renm;
	}

	public void setFile_renm(String file_renm) {
		this.file_renm = file_renm;
	}

	public String getFile_size() {
		return file_size;
	}

	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}

	
	@Override
	public String toString() {
		return "JspFileVo [file_no=" + file_no + ", posts_no=" + posts_no
				+ ", file_upnm=" + file_upnm + ", file_path=" + file_path
				+ ", file_renm=" + file_renm + ", file_size=" + file_size + "]";
	}
	
	
	
	


}
